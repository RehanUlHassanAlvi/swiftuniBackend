module.exports.addAdmin = async (req, res, next) => {
  try {
    const { email, name, password, permissions } = req.body;

    const values = [
      email.toLowerCase(),
      name,
      password,
      req.session.portalID,
      permissions,
    ];

    const result = await service.addAdmin(values);
    if (result) {
      res.setHeader(
        "Strict-Transport-Security",
        "max-age=31536000; includeSubDomains"
      );
      return res.json(result);
    }
    res.setHeader(
      "Strict-Transport-Security",
      "max-age=31536000; includeSubDomains"
    );
    return res.json(result);
  } catch (ex) {
    res.setHeader(
      "Strict-Transport-Security",
      "max-age=31536000; includeSubDomains"
    );
    return res.json({
      message: "Something Went Wrong",
    });
  }
};

