const service = require("./service");

module.exports.addCommentLike = async (req, res, next) => {
  try {
    const { attempted_questions_comment_id } = req.query;
    const values = [req.session.userID, attempted_questions_comment_id];
    const result = await service.addCommentLike(values);
    if (result) {
      res.setHeader(
        "Strict-Transport-Security",
        "max-age=31536000; includeSubDomains"
      );
      return res.json(result);
    }
    res.setHeader(
      "Strict-Transport-Security",
      "max-age=31536000; includeSubDomains"
    );
    return res.json(result);
  } catch (ex) {
    res.setHeader(
      "Strict-Transport-Security",
      "max-age=31536000; includeSubDomains"
    );
    return res.json({
      message: "Something Went Wrong",
    });
  }
};

